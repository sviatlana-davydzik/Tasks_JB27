package com.epam.jb27.lesson02;

public class Task03 {

	public static void main (String[] args) {
		
		double c, r, s;
		
		r = 5; 		
		
		double o = Math.PI;
		
		
		c = o * 2 * r; // длина окружности
		
		s = o * r * r; // площадь круга 
		
		System.out.println("Длина окружности: " + c + "\nПлощадь круга: " + s);
		
	}
	
}
